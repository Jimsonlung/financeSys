package com.linqiaoli.pojo;

public class Lesion {
    private String lesionId;
    private String lesionRoomId;
    private String name;
    private String address;

    public String getLesionId() {
        return lesionId;
    }

    public void setLesionId(String lesionId) {
        this.lesionId = lesionId;
    }

    public String getLesionRoomId() {
        return lesionRoomId;
    }

    public void setLesionRoomId(String lesionRoomId) {
        this.lesionRoomId = lesionRoomId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
