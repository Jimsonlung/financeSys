package com.linqiaoli.pojo;

public class LesionRoom {
    private String lesionRoomId;
    private String HospitalBed;
    private String name;
    private String address;

    public String getLesionRoomId() {
        return lesionRoomId;
    }

    public void setLesionRoomId(String lesionRoomId) {
        this.lesionRoomId = lesionRoomId;
    }

    public String getHospitalBed() {
        return HospitalBed;
    }

    public void setHospitalBed(String hospitalBed) {
        HospitalBed = hospitalBed;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
